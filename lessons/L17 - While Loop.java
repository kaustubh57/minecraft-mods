package in.seahagen.lessons;

public class Main {

    public static void main(String[] args)
	{
		/* WHILE LOOP */

		int x = 5;

		// WHILE
		while(x < 10)
		{
			System.out.println(x);
			x++;
		}

		// DO WHILE
		do {
			System.out.println(x);
			x++;
		}
		while(x < 15);
    }
}

