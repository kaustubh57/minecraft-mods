package in.seahagen.lessons;

public class Main {

    public static void main(String[] args)
	{
		/* FOR LOOP */

		// Counting from 0 to 5 (Forwards)
		for (int i = 0; i < 5; i++)
		{
			System.out.println(i);
		}

		// Counting from 10 to 0 (Backwards)
		for (int i = 10; i > 0; i--)
		{
			System.out.println(i);
		}
    }
}

